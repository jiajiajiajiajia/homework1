package homework;

public class 平行四边形 {

	public static void main(String[] args) {
		for(int a=1;a<=5;a++) {
			
			for (int b=1;b<=5-a;b++) {
					System.out.print(" ");
				}
			for(int c=1;c<=5;c++) {
					System.out.print(".");
				}
			
			
			
			System.out.println(" ");

			}
		
		
		}

}
